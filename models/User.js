const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// Create schema for a user
const UserSchema = new Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  mobilePhone: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }

  //TODO: Create avatar 

  
});

module.exports = User = mongoose.model('users', UserSchema);